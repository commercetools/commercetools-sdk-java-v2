
package com.commercetools.api.predicates.expansion.customer_group;

import static com.commercetools.api.predicates.expansion.ExpansionUtil.appendOne;

import java.util.Collections;
import java.util.List;

import com.commercetools.api.predicates.expansion.ExpansionDsl;

public class CustomerGroupExpansionBuilderDsl implements ExpansionDsl {

    private final List<String> path;

    private CustomerGroupExpansionBuilderDsl(final List<String> path) {
        this.path = path;
    }

    public static CustomerGroupExpansionBuilderDsl of() {
        return new CustomerGroupExpansionBuilderDsl(Collections.emptyList());
    }

    public static CustomerGroupExpansionBuilderDsl of(final List<String> path) {
        return new CustomerGroupExpansionBuilderDsl(path);
    }

    @Override
    public List<String> getPath() {
        return path;
    }

    public com.commercetools.api.predicates.expansion.common.LastModifiedByExpansionBuilderDsl lastModifiedBy() {
        return com.commercetools.api.predicates.expansion.common.LastModifiedByExpansionBuilderDsl
                .of(appendOne(path, "lastModifiedBy"));
    }

    public com.commercetools.api.predicates.expansion.common.CreatedByExpansionBuilderDsl createdBy() {
        return com.commercetools.api.predicates.expansion.common.CreatedByExpansionBuilderDsl
                .of(appendOne(path, "createdBy"));
    }

    public com.commercetools.api.predicates.expansion.type.CustomFieldsExpansionBuilderDsl custom() {
        return com.commercetools.api.predicates.expansion.type.CustomFieldsExpansionBuilderDsl
                .of(appendOne(path, "custom"));
    }
}
